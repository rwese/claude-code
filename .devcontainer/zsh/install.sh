# Uninstall function
uninstall() {
    log_info "Uninstalling Claude DevContainer ZSH configuration..."
    
    # Remove configuration directory
    if [[ -d "$ZSH_CONFIG_DIR" ]]; then
        rm -rf "$ZSH_CONFIG_DIR"
        log_success "Removed configuration directory"
    fi
    
    # Remove from ~/.zshrc
    if [[ -f "$HOME/.zshrc" ]]; then
        local temp_file=$(mktemp)
        grep -v "Claude DevContainer Configuration" "$HOME/.zshrc" > "$temp_file" || true
        grep -v "claude-devcontainer" "$temp_file" > "$HOME/.zshrc" || true
        rm "$temp_file"
        log_success "Removed configuration from ~/.zshrc"
    fi
    
    log_success "Uninstallation complete"
    log_info "Restart your terminal to complete the removal"
}

# Usage information
usage() {
    cat << 'EOF'
Claude DevContainer ZSH Configuration Installer

USAGE:
    ./install.sh [COMMAND]

COMMANDS:
    install     Install configuration (default)
    uninstall   Remove configuration
    test        Test existing configuration
    help        Show this help

EXAMPLES:
    ./install.sh                    # Install configuration
    ./install.sh test               # Test configuration
    ./install.sh uninstall          # Remove configuration

The installer will:
    â€¢ Install ZSH configuration files to ~/.config/zsh/claude-devcontainer/
    â€¢ Update ~/.zshrc to source the configuration
    â€¢ Create backups of existing configuration
    â€¢ Verify installation integrity

EOF
}

# Main execution
main() {
    local command="${1:-install}"
    
    case "$command" in
        install)
            echo "ğŸ”§ Installing Claude DevContainer ZSH Configuration"
            echo "=================================================="
            check_prerequisites
            backup_existing
            install_config_files
            update_zshrc
            verify_installation
            test_configuration
            show_summary
            ;;
        
        uninstall)
            echo "ğŸ—‘ï¸  Uninstalling Claude DevContainer ZSH Configuration"
            echo "======================================================"
            uninstall
            ;;
        
        test)
            echo "ğŸ§ª Testing Claude DevContainer ZSH Configuration"
            echo "=============================================="
            test_configuration
            log_success "Configuration is working correctly"
            ;;
        
        help|--help|-h)
            usage
            ;;
        
        *)
            log_error "Unknown command: $command"
            usage
            exit 1
            ;;
    esac
}

# Execute main function
main "$@"